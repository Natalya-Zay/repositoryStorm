package com.github.NatalyaZay.module01;

public class ProgramTask44 {
    public static void main(String[] args) {
        int a = 3;
        int b = 5;
        System.out.println(a>b);
        System.out.println(a<b);
        System.out.println(a>=b);
        System.out.println(a<=b);
        System.out.println(a==b);
        System.out.println(a!=b);
        System.out.println();
        boolean x = true;          // a != b
        System.out.println(x);
        System.out.println();

        int a1 = 5;
        int b1 = 5;
        System.out.println(a1>b1);// boolean . 5 не больше 5 false . так как 5 равно 5
        System.out.println(a1<b1); //  5 не больше 5 false . так как 5 равно 5
        System.out.println(a1>=b1); // 5 равно 5 , true
        System.out.println(a1<=b1); // 5 равно 5 , true
        System.out.println(a1==b1); // 5 равно 5 , true
        System.out.println(a1!=b1); // в выражении сказано , что 5 НЕ равно 5, это false
        System.out.println();
        boolean c = false;       // a != b
        System.out.println(c);



    }
}
